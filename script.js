        // ========== الدوال المساعدة ==========
        normalizeArabic(text) {
            let normalized = text.replace(/[\u064B-\u065F\u0670]/g, '');
            normalized = normalized.replace(/[أإآ]/g, 'ا');
            normalized = normalized.replace(/[يى]/g, 'ي');
            normalized = normalized.replace(/ؤ/g, 'و');
            normalized = normalized.replace(/ة/g, 'ه');
            normalized = normalized.replace(/\s+/g, ' ').trim();
            return normalized;
        },

        // ========== التاريخ الهجري ==========
        updateCurrentHijriDate() {
            const today = new Date();
            this.currentHijriDate = this.gregorianToHijri(today);
        },

        gregorianToHijri(date) {
            try {
                const formatter = new Intl.DateTimeFormat('ar-SA-u-ca-islamic-umalqura', {
                    year: 'numeric',
                    month: 'long',
                    day: 'numeric',
                    calendar: 'islamic-umalqura'
                });
                return formatter.format(date).replace(/هـ/, '').trim();
            } catch (e) {
                return this.fallbackHijri(date);
            }
        },

        convertManualGregorian() {
            if (!this.gregorianInputDate) {
                this.convertedHijriResult = '';
                return;
            }
            const date = new Date(this.gregorianInputDate);
            if (isNaN(date)) {
                this.convertedHijriResult = 'تاريخ غير صحيح';
                return;
            }
            this.convertedHijriResult = this.gregorianToHijri(date);
        },

        fallbackHijri(date) {
            const gYear = date.getFullYear();
            const gMonth = date.getMonth() + 1;
            const gDay = date.getDate();
            const jd = Math.floor((1461 * (gYear + 4800 + Math.floor((gMonth - 14) / 12))) / 4) +
                       Math.floor((367 * (gMonth - 2 - 12 * Math.floor((gMonth - 14) / 12))) / 12) -
                       Math.floor((3 * Math.floor((gYear + 4900 + Math.floor((gMonth - 14) / 12)) / 100)) / 4) +
                       gDay - 32075;
            const l = jd - 1948440 + 10632;
            const n = Math.floor((l - 1) / 10631);
            const l2 = l - 10631 * n + 354;
            const j = Math.floor((10985 - l2) / 5316) * Math.floor((50 * l2) / 17719) + Math.floor(l2 / 5670) * Math.floor((43 * l2) / 15238);
            const l3 = l2 - Math.floor((30 - j) / 15) * Math.floor((17719 * j) / 50) - Math.floor(j / 16) * Math.floor((15238 * j) / 43) + 29;
            const month = Math.floor((24 * l3) / 709);
            const day = l3 - Math.floor((709 * month) / 24);
            const year = 30 * n + j - 30;
            return `${day} ${this.getHijriMonthName(month)} ${year}`;
        },

        getHijriMonthName(month) {
            const months = ['محرم', 'صفر', 'ربيع الأول', 'ربيع الثاني', 'جمادى الأولى', 'جمادى الآخرة', 'رجب', 'شعبان', 'رمضان', 'شوال', 'ذو القعدة', 'ذو الحجة'];
            return months[month - 1] || '';
        },

